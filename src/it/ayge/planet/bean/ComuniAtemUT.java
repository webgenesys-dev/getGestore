package it.ayge.planet.bean;

public class ComuniAtemUT {
	
	private String nome;
	private String cod_istat;
	private String denominazione_gestore;
	private String nome_atem;
	private String provincia;
	private int pdr;
	private double qc_comune;
	private double qa_comune;
	private double totale;
	
			
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCod_istat() {
		return cod_istat;
	}
	public void setCod_istat(String cod_istat) {
		this.cod_istat = cod_istat;
	}
	public String getDenominazione_gestore() {
		return denominazione_gestore;
	}
	public void setDenominazione_gestore(String denominazione_gestore) {
		this.denominazione_gestore = denominazione_gestore;
	}
	public String getNome_atem() {
		return nome_atem;
	}
	public void setNome_atem(String nome_atem) {
		this.nome_atem = nome_atem;
	}
		
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public double getPdr() {
		return pdr;
	}
	public void setPdr(int pdr) {
		this.pdr = pdr;
	}
	public double getQc_comune() {
		return qc_comune;
	}
	public void setQc_comune(double qc_comune) {
		this.qc_comune = qc_comune;
	}
	public double getQa_comune() {
		return qa_comune;
	}
	public void setQa_comune(double qa_comune) {
		this.qa_comune = qa_comune;
	}
	public double getTotale() {
		return totale;
	}
	public void setTotale(double totale) {
		this.totale = totale;
	}
			

}
